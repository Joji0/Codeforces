const int maxN = 105;
vector<int> adj[maxN];
bool vis[maxN];

void dfs(int node) {
    if (vis[node])
        return;
    vis[node] = true;
    for (auto &x : adj[node]) {
        dfs(x);
    }
}

void solve() {
    // The idea to solve this problem is actually quite easy to see. We are just
    // asked to determine if the graph is both connected and contains exactly
    // one cycle where each node from this cycle are the root of some trees. To
    // check if such cycle exists, we just need to make sure that the #edges =
    // #vertices.
    int n, m;
    cin >> n >> m;
    for (int i = 0; i < m; i++) {
        int u, v;
        cin >> u >> v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    if (n != m) {
        // Check cycle
        cout << "NO\n";
        return;
    }
    dfs(1);
    for (int i = 1; i <= n; i++) {
        if (!vis[i]) {
            // Check connectivity
            cout << "NO\n";
            return;
        }
    }
    cout << "FHTAGN!\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
