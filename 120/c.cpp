void solve() {
    int n, k;
    cin >> n >> k;
    priority_queue<pair<int, int>> pq;
    for (int i = 0; i < n; i++) {
        int x;
        cin >> x;
        pq.push({x, 0});
    }
    int piglet = 0;
    while (!pq.empty()) {
        auto [f, cnt] = pq.top();
        pq.pop();
        if (f < k || cnt >= 3) {
            piglet += f;
        } else {
            pq.push({f - k, cnt + 1});
        }
    }
    cout << piglet << '\n';
}

