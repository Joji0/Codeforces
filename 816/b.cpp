const int maxN = 200005;

void solve() {
    int n, k, q;
    cin >> n >> k >> q;
    vector<int> diff(maxN, 0), recover(maxN, 0), pref(maxN, 0);
    for (int i = 0; i < n; i++) {
        int l, r;
        cin >> l >> r;
        diff[l - 1]++;
        diff[r]--;
    }
    recover[1] = diff[0];
    for (int i = 2; i < maxN; i++) {
        recover[i] = recover[i - 1] + diff[i - 1];
    }
    for (int i = 1; i < maxN; i++) {
        pref[i] = pref[i - 1] + (recover[i] >= k);
    }
    while (q--) {
        int l, r;
        cin >> l >> r;
        cout << pref[r] - pref[max(0, l - 1)] << '\n';
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
