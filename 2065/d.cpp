void solve() {
    int n, m;
    cin >> n >> m;
    vector<vector<int64_t>> a(n, vector<int64_t>(m));
    cin >> a;
    vector<pair<int64_t, int>> order(n);
    for (int i = 0; i < n; i++) {
        int64_t s = accumulate(a[i].begin(), a[i].end(), 0);
        order[i] = {s, i};
    }
    sort(order.begin(), order.end(),
         [](const pair<int64_t, int> &a, const pair<int64_t, int> &b) {
             return a.first > b.first;
         });
    int64_t ans = 0, last = 0;
    for (auto &[_, b] : order) {
        for (auto &x : a[b]) {
            ans += last + x;
            last += x;
        }
    }
    cout << ans << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
