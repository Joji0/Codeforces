void solve() {
    // The solution of this problem is simply to check whether there exists a
    // path of length 3 with at least 2 K value for K in the sequence a. For
    // example, K-X-K is already a majority for K and hence this is sufficient.
    int n;
    cin >> n;
    vector<int> a(n);
    vector<vector<int>> adj(n, vector<int>());
    cin >> a;
    for (int i = 0; i < n - 1; i++) {
        int u, v;
        cin >> u >> v;
        u--, v--;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    string ans(n, '0');
    for (int i = 0; i < n; i++) {
        map<int, int> mp;
        for (auto &x : adj[i]) {
            mp[a[x]]++;
        }
        mp[a[i]]++;
        for (auto &[v, val] : mp) {
            if (val >= 2) {
                ans[v - 1] = '1';
            }
        }
    }
    cout << ans << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
